  <h1>Medical Leave - Poliklinik Koh</h1>

  <h2>Overview</h2>
  <p>Medical leave at Poliklinik Koh is a benefit provided to employees to support their health and well-being. It allows employees to take time off work for medical reasons without loss of pay.</p>

  <h2>Eligibility</h2>
  <p>All regular full-time employees at Poliklinik Koh are eligible for medical leave after completing three months of continuous employment.</p>

  <h2>Duration and Accumulation</h2>
  <p>Employees are entitled to up to 14 days of medical leave per year. Unused medical leave does not carry over to the following year.</p>

  <h2>Request Process</h2>
  <p>To request medical leave, employees should inform their immediate supervisor or the HR department as soon as possible. Employees may be required to submit a medical certificate or relevant documentation depending on the duration of the leave requested.</p>

  <h2>Medical Documentation</h2>
  <p>For medical leave exceeding three consecutive days, employees are required to provide a medical certificate issued by a registered medical practitioner.</p>

  <h2>Rights and Protections</h2>
  <p>During medical leave, employees are protected from any adverse employment actions. They have the right to privacy and confidentiality regarding their medical condition.</p>

  <h2>Return-to-Work</h2>
  <p>Upon returning to work after medical leave, employees are expected to provide a fitness-for-work certificate from their healthcare provider, stating that they are fit to resume their duties.</p>

  <h2>Additional Support</h2>
  <p>Poliklinik Koh provides additional resources such as an Employee Assistance Program (EAP) offering counseling services and support for employees facing health challenges.</p>

